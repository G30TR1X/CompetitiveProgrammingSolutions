void solve()
{
    cin >> n;
    ll power[] = {2, 4, 8, 16, 32, 64, 128, 256, 512, 1024, 2048, 4096, 8192, 16384, 32768, 65536, 131072, 262144};
    ll ans = 0, digits = 1;
    for (ll i = 0; i < n; ++i)
    {
        if ((ans + digits) % power[i] == 0 || (ans + digits*2) % power[i] == 0)
        {
            ans += ((ans + digits) % power[i] == 0) ? digits : digits*2;
            digits *= 10;
            continue;
        }

        cout << -1 << '\n';
        return;
    }

    cout << ans << '\n';
}

